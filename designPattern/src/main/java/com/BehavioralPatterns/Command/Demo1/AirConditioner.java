package main.java.com.BehavioralPatterns.Command.Demo1;

/**
 * The type Air conditioner.
 *
 * @author Chetan Raj
 * @implNote
 * @since : 07/02/19
 */
public class AirConditioner {
	/**
	 * Off.
	 */
	public void off() {
		System.out.println("Air Conditioner is OFF now");
	}

	/**
	 * On.
	 */
	public void on() {
		System.out.println("Air Conditioner is ON now");
	}
}
